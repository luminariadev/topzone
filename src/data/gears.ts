export function getGearBySlug(slug: string): Gear | undefined {
  return gears.find((g) => g.slug === slug);
}

export function getGearCategories(): GearCategory[] {
  const cats = [...new Set(gears.map(g => g.category))];
  return cats.sort();
}

export function getGearsByCategory(category: GearCategory | 'all'): Gear[] {
  if (!category || category === 'all') return gears;
  return gears.filter(g => g.category === category);
}
